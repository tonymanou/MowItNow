package com.tonymanou.mowitnow.model;

/**
 * Describes a command to be given to a mower.
 */
public enum Command {

    /**
     * Indicate to the mower to turn right.
     */
    D,

    /**
     * Indicate to the mower to turn left.
     */
    G,

    /**
     * Indicate to the mower to move forward.
     */
    A
}
